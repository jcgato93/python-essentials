# Los slices son una forma de tomar porciones de un string

my_name = "electroencefalografista"

# Primera letra
print(my_name[0])

# Ultima letra
print(my_name[-1])

# De la primera a la cuarta no incluyente
print(my_name[0:3])

# Del inicio hasta el final del string saltando 2 caracteres
print(my_name[::2])
